"use strict";

let tarayici = 'Chrome';

switch (tarayici) {
    case 'Edge':
      alert( "Edge browser kullanıyorsun" );
      break;
  
    case 'Chrome':
        alert( "Chrome browser kullanıyorsun" );
        break;
    case 'Firefox':
        alert( "Firefox browser kullanıyorsun" );
        break;
    case 'Safari':
        alert( "Safari browser kullanıyorsun" );
        break;
    case 'Opera':
      alert( 'Opera bunları destekliyoruz.' );
      break;
  
    default:
      alert( 'Umarım sayfanız güzel görünüyordur' );
      break;
  };